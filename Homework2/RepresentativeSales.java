/*
Representative Sales class, every object has a name, sales and quota
 */

public class RepresentativeSales {
    String name;
    int repSales;
    int repQuota;

    public RepresentativeSales(String name, int repSales, int repQuota) {
        this.name = name;
        this.repSales = repSales;
        this.repQuota = repQuota;
    }

}
